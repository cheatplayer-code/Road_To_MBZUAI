"""
Math trick: Stable Softmax

If we take each element of array and subtract it with the max value of this array,

result of function won't change, but numbers will become negative or will equal to zero

Now will write this function

"""

import numpy as np

def stable_softmax(X):
    max_value = np.max(X, axis=1, keepdims=True)
    """
    So here we are working with dimensions. We need to find the maximum element
    for each line. So we write acix=1. But result will be for example (3, )
    We want to keep dimension so we write keepdims=True and result will be 
    (3, 1)
    
    """

    shifted_X = X - max_value

    exp_X = np.exp(shifted_X)

    """
    We are working with lines because one line == one object. 

    axis = 1 => choosing every lines
    
    """
    
    sum_exp_X = np.sum(exp_X, axis=1, keepdims=True)

    return exp_X / sum_exp_X


